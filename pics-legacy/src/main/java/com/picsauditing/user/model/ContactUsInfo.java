package com.picsauditing.user.model;

public class ContactUsInfo {

	private String csrPhoneNumber;
	private String csrPhoneNumberExtension;
	private String csrName;

	public String getCsrPhoneNumber() {
		return csrPhoneNumber;
	}

	public void setCsrPhoneNumber(String csrPhoneNumber) {
		this.csrPhoneNumber = csrPhoneNumber;
	}

	public String getCsrPhoneNumberExtension() {
		return csrPhoneNumberExtension;
	}

	public void setCsrPhoneNumberExtension(String csrPhoneNumberExtension) {
		this.csrPhoneNumberExtension = csrPhoneNumberExtension;
	}

	public String getCsrName() {
		return csrName;
	}

	public void setCsrName(String csrName) {
		this.csrName = csrName;
	}
}

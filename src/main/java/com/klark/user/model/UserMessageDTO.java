package com.klark.user.model;

import java.util.Date;

import com.klark.common.AbstractEntityDTO;

public class UserMessageDTO extends AbstractEntityDTO<UserMessage> {

    private Long id;

    private Date dateEntered;

    private Date dateOfDelivery;

    private String subject;

    private String messageBody;

    private String receipientFirstName;

    private String receipientLastName;

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    public Date getDateEntered() {
        return dateEntered;
    }

    public void setDateEntered(Date dateEntered) {
        this.dateEntered = dateEntered;
    }

    public Date getDateOfDelivery() {
        return dateOfDelivery;
    }

    public void setDateOfDelivery(Date dateOfDelivery) {
        this.dateOfDelivery = dateOfDelivery;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getMessageBody() {
        return messageBody;
    }

    public void setMessageBody(String messageBody) {
        this.messageBody = messageBody;
    }
}
